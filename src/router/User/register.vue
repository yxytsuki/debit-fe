<template>
    <div class="loginContainer">
        <el-form :model="ruleForm" :rules="rules"
         status-icon
         ref="ruleForm"
         label-position="left"
         label-width="0px"
         class="demo-ruleForm loginPage">
            <h3 class="title">用户注册</h3>
            <el-form-item prop="username">
                <el-input type="text"
                    v-model="ruleForm.username"
                    auto-complete="off"
                    placeholder="用户名"
                    prefix-icon="el-icon-s-custom"
                ></el-input>
            </el-form-item>
            <el-form-item prop="username">
                <el-input type="text"
                    v-model="ruleForm.nickName"
                    auto-complete="off"
                    placeholder="昵称"
                    prefix-icon="el-icon-s-custom"
                ></el-input>
            </el-form-item>
            <el-form-item prop="password">
                <el-input type="password"
                    v-model="ruleForm.password"
                    auto-complete="off"
                    placeholder="密码"
                    show-password
                    prefix-icon="el-icon-key"
                ></el-input>
            </el-form-item>
            <el-checkbox
                v-model="checked"
                class="rememberme"
            >记住密码</el-checkbox>
            <el-form-item style="width:100%;">
                <el-button type="primary" plain style="width:100%;" @click="handleSubmit" :loading="logining">注册</el-button>
            </el-form-item>
        </el-form>
    </div>
</template>

<script>
export default {
  data () {
    return {
      logining: false,
      ruleForm: {
        username: '',
        nickName: '',
        password: '',
        registerTime: ''
      },
      rules: {
        username: [{required: true, message: '请输入用户名', trigger: 'blur'}],
        nickName: [{required: true, message: '请输入昵称名', trigger: 'blur'}],
        password: [{required: true, message: '请输入密码', trigger: 'blur'}]
      },
      checked: false
    }
  },
  methods: {
    handleSubmit (event) {
      this.ruleForm.registerTime = new Date().getTime()
      console.log(this.ruleForm)
      this.$ajax.post('/register', this.ruleForm)
        .then(res => {
          this.logining = false
          // console.log(res)
          this.$alert('注册成功', 'tip', {
            confirmButtonText: 'ok'
          })
          // 注册成功转到登陆页面
          this.$router.push({path: '/login'})
        })
        .catch(err => {
          console.log(err)
        })
    }
  }
}
</script>

